    public function getFromExercise($exercise){
        $conn = $this->getEntityManager()->getConnection();

        $sql = '
            SELECT q.name as question_name, q.description as question_description, * FROM student_answer sa
            INNER JOIN question q ON q.id = sa.question_id
            INNER JOIN exercise e ON e.id = q.exercises_id
            WHERE e.id = :exercise
            ';
        $stmt = $conn->prepare($sql);
        $stmt->execute(['exercise' => $exercise->getId()]);
        return $stmt->fetchAllAssociative();
    }

